import axios from 'axios';

export class ExampleGeneratorService {
    constructor() {
        this.useYandex = !!process.env.YANDEX_DICTIONARY_API_KEY;
        console.log('üîß ExampleGeneratorService initialized, useYandex:', this.useYandex);
    }

    async generateExamples(word, translation) {
        console.log(`\nüîÑ ========== GENERATING EXAMPLES ==========`);
        console.log(`üîÑ Input: word="${word}", translation="${translation}"`);
        
        if (!this.useYandex) {
            console.log('‚ùå Yandex API key not available, using contextual examples');
            return this.generateContextualExamples(word, translation);
        }

        try {
            console.log('üîç PRIMARY: Trying Yandex API for examples...');
            const yandexExamples = await this.getYandexExamples(word);
            
            if (yandexExamples && yandexExamples.length > 0) {
                console.log(`‚úÖ PRIMARY SUCCESS: Found ${yandexExamples.length} examples from Yandex`);
                console.log('üìã Yandex examples:', yandexExamples);
                return yandexExamples;
            } else {
                console.log('‚ùå PRIMARY FAILED: No examples found in Yandex response');
                console.log('üîÑ FALLBACK: Using contextual examples');
                return this.generateContextualExamples(word, translation);
            }
        } catch (error) {
            console.log('‚ùå PRIMARY ERROR: Yandex examples failed:', error.message);
            console.log('üîÑ FALLBACK: Using contextual examples');
            return this.generateContextualExamples(word, translation);
        }
    }

    async getYandexExamples(word) {
        console.log(`\nüîç ========== YANDEX API CALL ==========`);
        console.log(`üîç Making request for word: "${word}"`);
        
        try {
            const params = {
                key: process.env.YANDEX_DICTIONARY_API_KEY,
                lang: 'en-ru',
                text: word,
                ui: 'ru',
                flags: 0x0004 // –§–ª–∞–≥ –¥–ª—è –ø—Ä–∏–º–µ—Ä–æ–≤
            };

            console.log('üîç Request params:', {
                key: '***' + (process.env.YANDEX_DICTIONARY_API_KEY ? process.env.YANDEX_DICTIONARY_API_KEY.slice(-4) : 'none'),
                lang: params.lang,
                text: params.text,
                ui: params.ui,
                flags: params.flags.toString(16)
            });

            const response = await axios.get('https://dictionary.yandex.net/api/v1/dicservice.json/lookup', {
                params: params,
                timeout: 10000
            });

            console.log('‚úÖ Yandex API response received');
            console.log('üìä Response status:', response.status);
            console.log('üìä Response has data:', !!response.data);
            
            return this.extractExamplesFromYandex(response.data, word);
            
        } catch (error) {
            console.error('‚ùå Yandex API request failed');
            console.error('Error message:', error.message);
            
            if (error.response) {
                console.error('Response status:', error.response.status);
                console.error('Response headers:', error.response.headers);
                if (error.response.data) {
                    console.error('Response data:', JSON.stringify(error.response.data, null, 2));
                }
            } else if (error.request) {
                console.error('No response received:', error.request);
            }
            
            console.error('Error config:', {
                url: error.config?.url,
                method: error.config?.method,
                params: error.config?.params
            });
            
            return [];
        }
    }

    extractExamplesFromYandex(data, originalWord) {
        console.log(`\nüîç ========== EXTRACTING EXAMPLES ==========`);
        
        if (!data) {
            console.log('‚ùå No data in response');
            return [];
        }

        console.log('üìä Response keys:', Object.keys(data));
        console.log('üìä Response code:', data.code);
        console.log('üìä Response nmt_code:', data.nmt_code);

        if (!data.def || !Array.isArray(data.def)) {
            console.log('‚ùå No "def" array in response');
            return [];
        }

        console.log(`üîç Found ${data.def.length} definition(s)`);

        const examples = [];
        let totalExamplesFound = 0;

        data.def.forEach((definition, defIndex) => {
            console.log(`\nüìñ Definition ${defIndex + 1}:`);
            console.log('   text:', definition.text);
            console.log('   pos:', definition.pos);
            console.log('   ts:', definition.ts);
            console.log('   keys:', Object.keys(definition));

            if (definition.tr && Array.isArray(definition.tr)) {
                console.log(`   üìö Found ${definition.tr.length} translation(s)`);
                
                definition.tr.forEach((translation, trIndex) => {
                    console.log(`   üîç Translation ${trIndex + 1}:`);
                    console.log('      text:', translation.text);
                    console.log('      pos:', translation.pos);
                    console.log('      keys:', Object.keys(translation));

                    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–ª–µ "ex"
                    if (translation.ex) {
                        console.log('      ‚úÖ HAS "ex" FIELD:', translation.ex);
                        if (Array.isArray(translation.ex)) {
                            console.log(`      üìù Found ${translation.ex.length} example(s) in 'ex' field`);
                            
                            translation.ex.forEach((example, exIndex) => {
                                if (totalExamplesFound >= 3) {
                                    console.log('      ‚èπÔ∏è  Skipping - reached limit');
                                    return;
                                }
                                
                                console.log(`      üîç Example ${exIndex + 1}:`);
                                console.log('         text:', example.text);
                                console.log('         tr:', example.tr);
                                console.log('         keys:', Object.keys(example));

                                if (example.text && example.tr && Array.isArray(example.tr) && example.tr[0]?.text) {
                                    const englishExample = example.text.trim();
                                    const russianExample = example.tr[0].text.trim();
                                    
                                    console.log('         ‚úÖ Valid example structure');
                                    console.log('         English:', englishExample);
                                    console.log('         Russian:', russianExample);
                                    
                                    const formattedExample = `${englishExample} - ${russianExample}`;
                                    examples.push(formattedExample);
                                    totalExamplesFound++;
                                    console.log(`         ‚úÖ ADDED: "${formattedExample}"`);
                                } else {
                                    console.log('         ‚ùå Invalid example structure');
                                }
                            });
                        } else {
                            console.log('      ‚ùå "ex" is not an array:', typeof translation.ex);
                        }
                    } else {
                        console.log('      ‚ùå NO "ex" FIELD in translation');
                    }

                    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–∏–Ω–æ–Ω–∏–º—ã
                    if (translation.syn && Array.isArray(translation.syn)) {
                        console.log(`      üîÑ Checking ${translation.syn.length} synonym(s) for examples...`);
                        translation.syn.forEach((synonym, synIndex) => {
                            if (synonym.ex) {
                                console.log(`      üìù Synonym ${synIndex + 1} HAS "ex":`, synonym.ex);
                            }
                        });
                    }
                });
            } else {
                console.log('   ‚ùå NO translations in definition');
            }
        });

        console.log(`\nüìä ========== EXTRACTION RESULTS ==========`);
        console.log(`üìä Total examples extracted: ${examples.length}`);
        
        if (examples.length === 0) {
            console.log('‚ùå No examples could be extracted from Yandex response');
            console.log('üí° Possible reasons:');
            console.log('   - –Ø–Ω–¥–µ–∫—Å –Ω–µ –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—è–µ—Ç –ø—Ä–∏–º–µ—Ä—ã –¥–ª—è —ç—Ç–æ–≥–æ —Å–ª–æ–≤–∞');
            console.log('   - –ü—Ä–∏–º–µ—Ä—ã –Ω–µ–¥–æ—Å—Ç—É–ø–Ω—ã –≤ –±–µ—Å–ø–ª–∞—Ç–Ω–æ–º —Ç–∞—Ä–∏—Ñ–µ');
            console.log('   - –°—Ç—Ä—É–∫—Ç—É—Ä–∞ –æ—Ç–≤–µ—Ç–∞ –æ—Ç–ª–∏—á–∞–µ—Ç—Å—è –æ—Ç –æ–∂–∏–¥–∞–µ–º–æ–π');
        } else {
            console.log('‚úÖ Examples found:', examples);
        }

        return examples;
    }

    generateContextualExamples(word, translation) {
        console.log(`\n‚úèÔ∏è ========== GENERATING CONTEXTUAL EXAMPLES ==========`);
        
        const examples = [
            `I often use the word "${word}" in my conversations. - –Ø —á–∞—Å—Ç–æ –∏—Å–ø–æ–ª—å–∑—É—é —Å–ª–æ–≤–æ "${translation}" –≤ —Ä–∞–∑–≥–æ–≤–æ—Ä–∞—Ö.`,
            `Can you give me an example with "${word}"? - –ú–æ–∂–µ—Ç–µ –ø—Ä–∏–≤–µ—Å—Ç–∏ –ø—Ä–∏–º–µ—Ä —Å "${translation}"?`,
            `The word "${word}" is very useful in English. - –°–ª–æ–≤–æ "${translation}" –æ—á–µ–Ω—å –ø–æ–ª–µ–∑–Ω–æ –≤ –∞–Ω–≥–ª–∏–π—Å–∫–æ–º —è–∑—ã–∫–µ.`
        ];

        console.log(`‚úÖ Generated ${examples.length} contextual examples`);
        console.log('üìã Examples:', examples);
        
        return examples;
    }

    formatExamplesForDisplay(examples) {
        if (!examples || !Array.isArray(examples) || examples.length === 0) {
            return '–ü—Ä–∏–º–µ—Ä—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã';
        }
        
        return examples.map((example, index) => {
            return `${index + 1}. ${typeof example === 'string' ? example : String(example)}`;
        }).join('\n');
    }

    formatExamplesForStorage(examples) {
        if (!examples || !Array.isArray(examples) || examples.length === 0) {
            return '';
        }
        
        return examples.map(example => 
            typeof example === 'string' ? example : String(example)
        ).join(' | ');
    }
}
